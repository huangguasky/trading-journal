import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const desktopDir = dirname(dirname(fileURLToPath(import.meta.url)));
const runtimeDir = join(desktopDir, 'src-tauri', 'binaries', 'engine-sidecar-runtime');
const placeholder = join(runtimeDir, '.development-placeholder');

mkdirSync(runtimeDir, { recursive: true });
if (!existsSync(placeholder)) {
  writeFileSync(placeholder, 'The packaged sidecar is generated by tools/build_sidecar.py.\n');
}
